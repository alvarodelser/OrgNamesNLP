"""
run_finetuned_experiments.py
============================
Runs similarity + classifier embedding experiments for the locally
finetuned mE5 model ('finetuned-me5') using the same train / validation
splits used for all other experiments.

Prereqs
-------
- results/embeddings/finetuned-me5_embeddings.csv  must already exist
  (generated by generate_finetuned_embeddings.py on the server)
- results/embeddings/label_embeddings.csv  must contain a
  'finetuned-me5_embedding' column (also done by generate_finetuned_embeddings.py)

Usage
-----
    python run_finetuned_experiments.py [--similarity-only] [--classifier-only]
                                        [--classifiers logreg svm]
                                        [--overwrite]
"""

import argparse
import pandas as pd
from sklearn.model_selection import train_test_split

from orgpackage.aux import load_dataset
from orgpackage.orchestrator import similarity_orchestrator, classifier_orchestrator

MODEL_KEY = 'finetuned-me5'


def build_splits(data):
    """Reproduce the exact train/val splits from orchestrator_dev.ipynb."""
    # Medical / Administrative domain
    train_medgov_full, _ = train_test_split(data, test_size=0.5, random_state=42)
    train_medgov, val_medgov = train_test_split(train_medgov_full, test_size=0.5, random_state=42)

    # Education domain (larger set, different split)
    train_edu_full, _ = train_test_split(data, test_size=0.2, random_state=42)
    train_edu, val_edu = train_test_split(train_edu_full, test_size=0.5, random_state=42)

    trains = {
        'medical':        train_medgov,
        'administrative': train_medgov,
        'education':      train_edu,
    }
    validations = {
        'medical':        val_medgov,
        'administrative': val_medgov,
        'education':      val_edu,
    }
    return trains, validations


def main():
    parser = argparse.ArgumentParser(description="Run finetuned-me5 embedding experiments")
    parser.add_argument('--similarity-only', action='store_true',
                        help="Run only cosine-similarity experiments")
    parser.add_argument('--classifier-only', action='store_true',
                        help="Run only classifier-head experiments")
    parser.add_argument('--classifiers', nargs='+', default=None,
                        choices=['logreg', 'svm'],
                        help="Classifier types to use (default: both)")
    parser.add_argument('--overwrite', action='store_true',
                        help="Overwrite existing results for this model")
    args = parser.parse_args()

    run_similarity  = not args.classifier_only
    run_classifiers = not args.similarity_only

    print("Loading dataset …")
    data = load_dataset()
    trains, validations = build_splits(data)

    # ------------------------------------------------------------------ #
    # 1. COSINE SIMILARITY experiments                                     #
    # ------------------------------------------------------------------ #
    if run_similarity:
        print(f"\n=== Similarity experiments  [{MODEL_KEY}] ===")
        similarity_orchestrator(trains, validations, models=[MODEL_KEY])

    # ------------------------------------------------------------------ #
    # 2. CLASSIFIER HEAD experiments                                       #
    # ------------------------------------------------------------------ #
    if run_classifiers:
        print(f"\n=== Classifier experiments  [{MODEL_KEY}] ===")
        classifier_orchestrator(
            trains, validations,
            models=[MODEL_KEY],
            classifiers=args.classifiers,
            overwrite=args.overwrite,
        )

    print("\nDone. Results saved to results/experiments.csv")


if __name__ == "__main__":
    main()
